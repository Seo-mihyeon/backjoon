import java.io.*;
import java.util.*;
/*
 * 문제 번호 : #15894
 * 문제 이름 : 수학은 체육과목 입니다
 */
public class Main {
    public static void main(String[] args) throws IOException {
        BufferedReader bf = new BufferedReader(new InputStreamReader(System.in));

        long n = Long.parseLong(bf.readLine());

        System.out.println(n*4);


		bf.close();

	}
}